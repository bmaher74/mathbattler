#!/usr/bin/env node
/**
 * Writes repo-root runtime-config.js from process.env (and repo-root .env via loadRootDotEnv).
 * Run during `npm run build` so Netlify and static hosts serve /runtime-config.js without the dev server.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { loadRootDotEnvIntoProcessEnv } from "./loadRootDotEnv.mjs";
import { runtimeConfigJs } from "./runtimeConfigJs.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const OUT = path.join(ROOT, "runtime-config.js");

loadRootDotEnvIntoProcessEnv(ROOT);

const body = runtimeConfigJs(process.env, {
    bannerLines: [
        "// Generated by scripts/generate-runtime-config.mjs at build time.",
        "// Values from process.env / Netlify env (and local .env when run locally)."
    ]
});

fs.writeFileSync(OUT, body, "utf8");
console.log(`Wrote ${path.relative(ROOT, OUT)}`);
